def generate_tailwind() -> str:
    data = _load_foundations()
    colors = data["colors"]
    spacing = data["spacing"]
    typography = [t for t in data["typography"] if t.get("platform") == "ios"]
    corners = data["corners"]
    elevation = [e for e in data["elevation"] if e.get("material") == "none"]

    lines = []
    lines.append("/** @type {import('tailwindcss').Config} */")
    lines.append("// Generated by OneMore \u2014 Apple HIG Design Tokens")
    lines.append("module.exports = {")
    lines.append("  theme: {")
    lines.append("    extend: {")

    # --- Colors ---
    lines.append("      colors: {")

    cats = _categorize_colors(colors)

    # Apple system colors
    lines.append("        apple: {")
    for c in cats["system"]:
        name = c["keyword"].replace("system", "").lower()
        lines.append(f"          {name}: {{ light: '{c['light_hex']}', dark: '{c['dark_hex']}' }},")
    lines.append("        },")

    # Grays
    lines.append("        gray: {")
    for c in cats["grays"]:
        idx = c["keyword"].replace("systemGray", "")
        key = idx if idx else "DEFAULT"
        lines.append(f"          '{key}': {{ light: '{c['light_hex']}', dark: '{c['dark_hex']}' }},")
    lines.append("        },")

    # Labels
    lines.append("        label: {")
    for c in cats["labels"]:
        kw = c["keyword"]
        if kw == "label":
            key = "DEFAULT"
        else:
            key = kw.replace("Label", "").replace("label", "")
            key = key[0].lower() + key[1:] if key else "DEFAULT"
            if key == "secondary":
                pass
            elif key == "tertiary":
                pass
            elif key == "quaternary":
                pass
        lines.append(f"          {key}: {{ light: '{c['light_hex']}', dark: '{c['dark_hex']}' }},")
    lines.append("        },")

    # Backgrounds
    lines.append("        bg: {")
    for c in cats["backgrounds"]:
        kw = c["keyword"]
        if kw == "systemBackground":
            key = "DEFAULT"
        elif kw == "secondarySystemBackground":
            key = "secondary"
        elif kw == "tertiarySystemBackground":
            key = "tertiary"
        elif kw == "systemGroupedBackground":
            key = "grouped"
        elif kw == "secondarySystemGroupedBackground":
            key = "groupedSecondary"
        elif kw == "tertiarySystemGroupedBackground":
            key = "groupedTertiary"
        else:
            key = _keyword_to_slug(kw)
        lines.append(f"          '{key}': {{ light: '{c['light_hex']}', dark: '{c['dark_hex']}' }},")
    lines.append("        },")

    lines.append("      },")

    # --- Spacing ---
    lines.append("      spacing: {")
    for s in spacing:
        kw = s["keyword"]
        # Use px values for web
        lines.append(f"        'apple-{kw}': '{s['value_px']}px',")
    lines.append("      },")

    # --- Font Size ---
    lines.append("      fontSize: {")
    for t in typography:
        slug = _keyword_to_slug(t["keyword"])
        size = t["size_pt"]
        lh = t["line_height"]
        weight = _weight_to_css(t["weight"])
        lines.append(f"        '{slug}': ['{size}px', {{ lineHeight: '{lh}px', fontWeight: '{weight}' }}],")
    lines.append("      },")

    # --- Border Radius ---
    lines.append("      borderRadius: {")
    for c in corners:
        if c["radius_pt"] == "varies":
            continue
        slug = _keyword_to_slug(c["keyword"])
        lines.append(f"        'apple-{slug}': '{c['radius_pt']}px',")
    lines.append("      },")

    # --- Font Family ---
    lines.append("      fontFamily: {")
    lines.append("        apple: ['-apple-system', 'BlinkMacSystemFont', '\"Inter\"', 'system-ui', 'sans-serif'],")
    lines.append("      },")

    # --- Box Shadow ---
    lines.append("      boxShadow: {")
    for e in elevation:
        slug = _keyword_to_slug(e["keyword"])
        blur = e["blur"]
        opacity = e["opacity"]
        offset_y = e["offset_y"]
        lines.append(f"        '{slug}': '0 {offset_y}px {blur}px rgba(0,0,0,{opacity})',")
    lines.append("      },")

    lines.append("    },")
    lines.append("  },")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


