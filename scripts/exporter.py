def _categorize_colors(colors):
    """Split colors into system colors, grays, labels, backgrounds, fills, separators, and web."""
    system = []
    grays = []
    labels = []
    backgrounds = []
    fills = []
    separators = []
    web = []
    other = []

    for c in colors:
        kw = c["keyword"]
        role = c.get("semantic_role", "")
        platform = c.get("platform", "all")

        if platform == "web":
            web.append(c)
        elif kw.startswith("systemGray"):
            grays.append(c)
        elif kw.startswith("system") and not kw.startswith("systemGray"):
            if "Background" in kw or "Grouped" in kw:
                backgrounds.append(c)
            elif "Fill" in kw:
                fills.append(c)
            else:
                system.append(c)
        elif "label" in kw.lower() or "Label" in kw:
            labels.append(c)
        elif "background" in kw.lower() or "Background" in kw:
            backgrounds.append(c)
        elif "fill" in kw.lower() or "Fill" in kw:
            fills.append(c)
        elif "separator" in kw.lower() or "Separator" in kw:
            separators.append(c)
        else:
            other.append(c)

    return {
        "system": system,
        "grays": grays,
        "labels": labels,
        "backgrounds": backgrounds,
        "fills": fills,
        "separators": separators,
        "web": web,
        "other": other,
    }


