def _load_foundations():
    """Load all foundation CSVs and return as a dict."""
    return {
        "colors": _load_csv("foundations", "colors.csv"),
        "spacing": _load_csv("foundations", "spacing.csv"),
        "typography": _load_csv("foundations", "typography.csv"),
        "corners": _load_csv("foundations", "corners.csv"),
        "elevation": _load_csv("foundations", "elevation.csv"),
    }


def _keyword_to_slug(keyword: str) -> str:
    """Convert camelCase keyword to kebab-case slug."""
    s = re.sub(r"([a-z0-9])([A-Z])", r"\1-\2", keyword)
    return s.lower().replace("_", "-")


def _keyword_to_snake(keyword: str) -> str:
    """Convert camelCase keyword to snake_case."""
    s = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", keyword)
    return s.lower()


def _camel_to_title(keyword: str) -> str:
    """Convert camelCase to Title Case for Swift/Dart identifiers."""
    # Keep the camelCase as-is for code identifiers
    return keyword


def _weight_to_css(weight: str) -> str:
    """Map weight name to CSS numeric weight."""
    mapping = {
        "Regular": "400",
        "Medium": "500",
        "Semibold": "600",
        "Bold": "700",
        "Heavy": "800",
        "Black": "900",
        "Light": "300",
        "Thin": "200",
        "Ultralight": "100",
    }
    return mapping.get(weight, "400")


def _weight_to_flutter(weight: str) -> str:
    """Map weight name to Flutter FontWeight."""
    mapping = {
        "Regular": "FontWeight.w400",
        "Medium": "FontWeight.w500",
        "Semibold": "FontWeight.w600",
        "Bold": "FontWeight.w700",
        "Heavy": "FontWeight.w800",
        "Black": "FontWeight.w900",
        "Light": "FontWeight.w300",
        "Thin": "FontWeight.w200",
        "Ultralight": "FontWeight.w100",
    }
    return mapping.get(weight, "FontWeight.w400")


