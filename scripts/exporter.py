def generate_css() -> str:
    data = _load_foundations()
    colors = data["colors"]
    spacing = data["spacing"]
    typography = [t for t in data["typography"] if t.get("platform") == "ios"]
    corners = data["corners"]
    elevation = [e for e in data["elevation"] if e.get("material") == "none"]

    lines = []
    lines.append("/* Generated by OneMore \u2014 Apple HIG Design Tokens */")
    lines.append(":root {")

    # Colors (light mode)
    lines.append("  /* Colors */")
    for c in colors:
        slug = _keyword_to_slug(c["keyword"])
        lines.append(f"  --{slug}: {c['light_hex']};")

    lines.append("")

    # Spacing
    lines.append("  /* Spacing */")
    for s in spacing:
        lines.append(f"  --space-{s['keyword']}: {s['value_px']}px;")

    lines.append("")

    # Typography
    lines.append("  /* Typography */")
    lines.append("  --font-apple: -apple-system, BlinkMacSystemFont, \"Inter\", system-ui, sans-serif;")
    for t in typography:
        slug = _keyword_to_slug(t["keyword"])
        lines.append(f"  --text-{slug}: {t['size_pt']}px;")
        lines.append(f"  --lh-{slug}: {t['line_height']}px;")

    lines.append("")

    # Border Radius
    lines.append("  /* Border Radius */")
    for c in corners:
        if c["radius_pt"] == "varies":
            continue
        slug = _keyword_to_slug(c["keyword"])
        lines.append(f"  --radius-{slug}: {c['radius_pt']}px;")

    lines.append("")

    # Shadows
    lines.append("  /* Shadows */")
    for e in elevation:
        slug = _keyword_to_slug(e["keyword"])
        lines.append(f"  --shadow-{slug}: 0 {e['offset_y']}px {e['blur']}px rgba(0,0,0,{e['opacity']});")

    lines.append("}")
    lines.append("")

    # Dark mode
    lines.append("@media (prefers-color-scheme: dark) {")
    lines.append("  :root {")
    lines.append("    /* Colors — dark mode overrides */")
    for c in colors:
        if c["light_hex"] != c["dark_hex"]:
            slug = _keyword_to_slug(c["keyword"])
            lines.append(f"    --{slug}: {c['dark_hex']};")
    lines.append("  }")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


