def generate_json() -> str:
    data = _load_foundations()

    result = {
        "generated_by": "OneMore \u2014 Apple HIG Design Tokens",
        "colors": {},
        "spacing": {},
        "typography": {},
        "borderRadius": {},
        "shadows": {},
    }

    for c in data["colors"]:
        result["colors"][c["keyword"]] = {
            "light": c["light_hex"],
            "dark": c["dark_hex"],
        }

    for s in data["spacing"]:
        result["spacing"][s["keyword"]] = int(s["value_pt"])

    for t in data["typography"]:
        result["typography"][t["keyword"]] = {
            "size": int(t["size_pt"]),
            "weight": t["weight"],
            "lineHeight": t["line_height"],
            "platform": t.get("platform", "ios"),
        }

    for c in data["corners"]:
        if c["radius_pt"] == "varies":
            continue
        parts = c["keyword"].split("-")
        key = parts[0] + "".join(p.capitalize() for p in parts[1:])
        result["borderRadius"][key] = int(c["radius_pt"])

    for e in data["elevation"]:
        slug = e["keyword"]
        result["shadows"][slug] = {
            "blur": int(e["blur"]) if e["blur"].isdigit() else e["blur"],
            "opacity": float(e["opacity"]),
            "offsetY": int(e["offset_y"]) if e["offset_y"].lstrip("-").isdigit() else e["offset_y"],
            "color": e["color"],
            "material": e["material"],
        }

    return _json.dumps(result, indent=2, ensure_ascii=False) + "\n"


