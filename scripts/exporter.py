_SWIFTUI_FONT_MAP = {
    "extraLargeTitle": ".extraLargeTitle",
    "extraLargeTitle2": ".extraLargeTitle2",
    "largeTitle": ".largeTitle",
    "title1": ".title",
    "title2": ".title2",
    "title3": ".title3",
    "headline": ".headline",
    "body": ".body",
    "callout": ".callout",
    "subheadline": ".subheadline",
    "footnote": ".footnote",
    "caption1": ".caption",
    "caption2": ".caption2",
}


def generate_swiftui() -> str:
    data = _load_foundations()
    colors = _categorize_colors(data["colors"])
    spacing = data["spacing"]
    typography = [t for t in data["typography"] if t.get("platform") == "ios"]
    corners = data["corners"]

    lines = []
    lines.append("// Generated by OneMore \u2014 Apple HIG Design Tokens")
    lines.append("import SwiftUI")
    lines.append("")

    # Color extension
    lines.append("extension Color {")
    for c in colors["system"]:
        name = c["keyword"].replace("system", "")
        swift_name = "apple" + name
        lines.append(f'    static let {swift_name} = Color("system{name}")')
    lines.append("    // Note: System colors are built-in to SwiftUI")
    lines.append("    // This file provides custom semantic tokens")
    lines.append("}")
    lines.append("")

    # Font extension
    lines.append("extension Font {")
    for t in typography:
        kw = t["keyword"]
        swift_name = "apple" + kw[0].upper() + kw[1:]
        mapped = _SWIFTUI_FONT_MAP.get(kw, ".body")
        lines.append(f"    static let {swift_name}: Font = {mapped}")
    lines.append("}")
    lines.append("")

    # Spacing struct
    lines.append("struct AppleSpacing {")
    for s in spacing:
        kw = s["keyword"]
        # Convert keyword to valid Swift identifier
        swift_kw = kw.replace("-", "_").replace(" ", "_")
        # Sanitize: if starts with digit, prefix with underscore
        if swift_kw[0].isdigit():
            swift_kw = "_" + swift_kw
        lines.append(f"    static let {swift_kw}: CGFloat = {s['value_pt']}")
    lines.append("}")
    lines.append("")

    # Radius struct
    lines.append("struct AppleRadius {")
    for c in corners:
        if c["radius_pt"] == "varies":
            continue
        kw = c["keyword"].replace("-", "")
        # camelCase
        parts = c["keyword"].split("-")
        swift_kw = parts[0] + "".join(p.capitalize() for p in parts[1:])
        lines.append(f"    static let {swift_kw}: CGFloat = {c['radius_pt']}")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


