EXPORT_FORMATS = {
    "tailwind": {"filename": "tailwind.config.js", "generator": generate_tailwind},
    "css": {"filename": "apple-tokens.css", "generator": generate_css},
    "swiftui": {"filename": "AppleTokens.swift", "generator": generate_swiftui},
    "flutter": {"filename": "apple_theme.dart", "generator": generate_flutter},
    "scss": {"filename": "_apple-tokens.scss", "generator": generate_scss},
    "json": {"filename": "apple-tokens.json", "generator": generate_json},
    "react-native": {"filename": "appleTheme.ts", "generator": generate_react_native},
}


def export_tokens(format_name: str, output_dir=None):
    """Export Apple HIG design tokens in the specified format."""
    if format_name not in EXPORT_FORMATS:
        return {"error": f"Unknown format: {format_name}", "available": list(EXPORT_FORMATS.keys())}

    config = EXPORT_FORMATS[format_name]
    content = config["generator"]()

    output_dir = Path(output_dir) if output_dir else Path.cwd()
    output_dir.mkdir(parents=True, exist_ok=True)
    filepath = output_dir / config["filename"]
    filepath.write_text(content, encoding="utf-8")

    return {
        "status": "generated",
        "format": format_name,
        "file": str(filepath),
        "filename": config["filename"],
    }
