_CUPERTINO_COLORS = {
    "systemBlue", "systemGreen", "systemIndigo", "systemOrange",
    "systemPink", "systemPurple", "systemRed", "systemTeal", "systemYellow",
}


def generate_flutter() -> str:
    data = _load_foundations()
    colors = data["colors"]
    spacing = data["spacing"]
    typography = [t for t in data["typography"] if t.get("platform") == "ios"]
    corners = data["corners"]

    lines = []
    lines.append("// Generated by OneMore \u2014 Apple HIG Design Tokens")
    lines.append("import 'package:flutter/cupertino.dart';")
    lines.append("")

    # Colors class
    lines.append("class AppleColors {")
    for c in colors:
        kw = c["keyword"]
        if kw in _CUPERTINO_COLORS:
            cupertino_name = kw
            lines.append(f"  static const {kw} = CupertinoColors.{cupertino_name};")
        elif c.get("platform") == "web":
            # Web colors as custom Color
            hex_val = c["light_hex"].lstrip("#")
            if len(hex_val) == 6:
                lines.append(f"  static const Color {kw} = Color(0xFF{hex_val.upper()});")
            else:
                lines.append(f"  static const Color {kw} = Color(0xFF{hex_val[:6].upper()});")
        else:
            hex_val = c["light_hex"].lstrip("#")
            if len(hex_val) <= 6:
                lines.append(f"  static const Color {kw} = Color(0xFF{hex_val.upper()});")
            else:
                # Has alpha
                alpha = hex_val[6:]
                rgb = hex_val[:6]
                lines.append(f"  static const Color {kw} = Color(0x{alpha.upper()}{rgb.upper()});")
    lines.append("}")
    lines.append("")

    # Spacing class
    lines.append("class AppleSpacing {")
    for s in spacing:
        kw = s["keyword"].replace("-", "_")
        if kw[0].isdigit():
            kw = "_" + kw
        lines.append(f"  static const double {kw} = {s['value_pt']};")
    lines.append("}")
    lines.append("")

    # Radius class
    lines.append("class AppleRadius {")
    for c in corners:
        if c["radius_pt"] == "varies":
            continue
        parts = c["keyword"].split("-")
        dart_kw = parts[0] + "".join(p.capitalize() for p in parts[1:])
        lines.append(f"  static const double {dart_kw} = {c['radius_pt']};")
    lines.append("}")
    lines.append("")

    # Typography class
    lines.append("class AppleTypography {")
    for t in typography:
        kw = t["keyword"]
        size = t["size_pt"]
        weight = _weight_to_flutter(t["weight"])
        lines.append(f"  static const TextStyle {kw} = TextStyle(fontSize: {size}, fontWeight: {weight});")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


